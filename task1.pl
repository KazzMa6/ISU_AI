% ФАКТЫ: кто есть кто
dog(flash).
dog(rover).
cat(bootsy).
horse(star).

% ФАКТЫ: цвет
black(flash).
brown(bootsy).
red(rover).
white(star).

% ФАКТЫ: владельцы
owner(tom, flash).   % Том владеет Флэш (собака, черная)
owner(tom, bootsy).  % Том владеет Бутси (кошка, коричневая)
owner(kate, rover).  % Кейт владеет Ровер (собака, рыжая)
owner(kate, star).   % Кейт владеет Стар (лошадь, белая)

% ПРАВИЛА

% Домашнее животное = собака ИЛИ кошка
pet(X) :- dog(X).
pet(X) :- cat(X).

% Животное = домашнее животное ИЛИ лошадь
animal(X) :- pet(X).
animal(X) :- horse(X).

% У Тома есть собака не черного цвета
% Факты уже есть: owner(tom, bootsy) - Бутси кошка, не подходит
% owner(tom, flash) - Флэш черная, не подходит
% Но нужна собака НЕ черная - таких у Тома нет по условию
% Поэтому это правило можно не добавлять, оно не выполняется

% У Кейта есть лошадь или что-то черного цвета
kate_has_horse_or_black :-
    owner(kate, X),
    (horse(X); black(X)).